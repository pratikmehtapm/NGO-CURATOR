                  <div class="modal-body">
                   “Even if he is a donkey, he is our only son,” says the three-foot tall dhobi, and the light fades out. There is a thundering applause.  The ‘villagers’ and the dancers dance to a Kannada folk song. As the music fades, the children bow to the audience. This time the applauses don’t cease immediately. And that’s how children of the Government schools were rewarded for their four months of hard work. <br>
From mid-June to the first week of October, these children came to the rehearsals at six in the morning, every day.  After a three hour rehearsal every morning, they got back to their schools. They even sacrificed celebr    ating festivals and family occasions to be able to make this event a stellar success.   <br>
During these months, the children were oriented to different aspects of theatre and were continually encouraged to look inwards. When they unearthed their talents buried deep within, their outlook towards life changed completely and they began a journey of self-discovery and achievement. This was reflected on 8th of October 2014, when The Akshaya Patra Foundation hosted the 2nd edition of Theatre For A Cause (TFAC) by staging the play – ‘Making a Man of an Ass’. <br>
The play revolves around a traditional folk tale that communicates subtle social messages of parent’s dreams and desires for their children, sometimes unreasoned and the pressure that it puts on the children’s lives. It talks about the importance of complete acceptance and selfless love in today’s world. <br>
TFAC is an initiative that attempts to enable deeper bonding between The Akshaya Patra Foundation, its donors and children, by providing an opportunity for children studying in the Government schools to explore their hidden potential and showcasing their talent to its invaluable donors. Initiated in 2012, the appreciations TFAC received and the difference it made in the lives of children made the organisation to bring it back again. This time, bigger and better! <br>
Staged through the medium of dance, drama and music, the script was developed in a participative process, incorporating the children’s perspectives and real-life experiences.  The play was a combination of three languages – Kannada, English and Hindi, keeping in mind the comfort factor of the children. The play was directed by Aantarya Film and Theatre House headed by theatre veteran Ms. Padmavathi Rao.  
 Sharing their experiences, Vijay, who played the role of a Maulvi says, “We learnt various aspects about theatre and also very important values. For instance, I learnt how we should not resort to shouting or beating if we were angry with someone else.”  Ashwini adds, “Initially we were not confident about performing the play because it was in English. So we developed some of the difficult dialogues in Kannada, but at the same time we were also encouraged to attempt to learn some of the English dialogues as well. Pinty akka (sister) would tell us the meaning and that helped us to understand and learn the lines.” <br>
The evening also saw the release of the in-house graphic book –“A Window into our Lives.” It was released by S. Jayakumar, Director – DSERT, Bangalore, (Former Joint Director Mid-Day Meal Programme).  The graphic book throws light on how the children perceive the mid-day meal with respect to education, socio-economic conditions, etc. through the medium of art. It is an emotional account of their day to day challenges and tribulations, joys and sorrows. It has utilized process-oriented artworks to explore the potential of children to express their lives and feelings. <br>
For the children and their families, donors, Government officials and the Foundation, the performance of the children on 8th October 2014 at Bangalore’s Chowdiah Memorial Hall will be etched in their memories. It will be testimonial for the potential of the millions of children studying in Government schools across the country. <br>


