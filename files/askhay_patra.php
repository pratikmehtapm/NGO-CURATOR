                    Proudly holding a certificate of participation awarded by the Government of Bihar and The Akshaya Patra Foundation, Divya Devi, a cook-cum-helper at the Government Primary School Fatehpur Dakshini, Vaishali District, Bihar, says, "I was aware of some of the hygiene and safety practices, but I learnt some more today. For instance, I didn't know we had to remove our bangles and finger rings while cooking. <br>The training programme taught us lessons that we will not forget."
 On an invitation from the Ministry of Human Resource Development (MHRD), Department of School Education and Literacy, Government of India, and the Government of Bihar, The Akshaya Patra Foundation organised a series of three one-day workshops on food safety, quality and personal hygiene in Patna, Bihar, from 29 to 31 August, 2013. The training programme took place in Government Middle School Kumhara, Patna, for cooks-cum-helpers from three districts namely Vaishali, Saran and Nalanda and for Block Resource Persons (BRPs), District Resource Persons (DRPs) and District Coordinators (DCs) from over 25 districts. The training witnessed a total participation from about 160 people. <br/>
With the MDMS currently reaching out to over 12 crore children across the country, a need to train the cook-cum-helper community to adopt safe practices was imperative. <br>Ministry of Human Resource Development, together with the state governments has collaborated with voluntary organisations to begin implementing a series of training programmes. <br>  Akshaya Patra was recognised as one of the organisations to develop and implement a few of the training programmes.
