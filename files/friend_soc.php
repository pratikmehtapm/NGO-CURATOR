                    Shaurya (“A feat of Courage”) is sports meet for the differently-abled children (physically, visually, hearing, or mentally challenged, or visually impaired) aimed at helping them discover and test their potential and infuse in them a spirit of fair and healthy competition. </br> About 500 to 600 children from 20 to 25 special schools in Vadodara participate. Various sporting events are organized, such as ‘Aim the Tin,’ ‘Ball Throw,’ ‘Find the Marble,’ ‘Identify the Object,’ ‘Ball Throw,’ ‘Sack Race,’ ‘Lemon and Spoon Race,’ ’50 m and 100 m Race,’ ‘Tricycle Race,” etc.
Friends Society has organized for the past 24 years annual sports meet for children with special needs (children with physical challenges, hearing impairment, visual impairments, limited mental abilities, and so on).</br></br></br>
Meticulous planning is the key to its success. Coordinating this annual event takes months of preparation by large number of volunteers. And, on the day of the event, more than 100 volunteers of all age groups pitch in to make it a success. This program also provides the volunteers opportunities to gain experience in organizing large events, working with many different people at various levels and from different organizations and fields, and dealing with obstacles that may arise. The self-development that occurs as a result of facing and resolving the challenges and implementation of these activities is quite significant.</br></br></br>
An event of this magnitude also requires significant financial support. Every year, we are able to secure help from large and small businesses and generous individuals. They help in different ways in cash or in kind. Some sponsor the prizes, such as the medals and certificates; some provide t-shirts, food, etc.

